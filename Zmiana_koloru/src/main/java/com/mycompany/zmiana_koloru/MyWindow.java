    private Color getColour(){
        r = jS_red.getValue();
        g = jS_green.getValue();
        b = jS_blue.getValue();
        Hex();
        setNumberRGB();
        return new Color(r,g,b);      
    }
    
    private void setNumberRGB(){
        jL_red.setText(r+"");
        jL_green.setText(g+"");
        jL_blue.setText(b+"");
    }
    
    private void Hex(){
        jTF_html.setText("#"+Integer.toHexString(r)+Integer.toHexString(g)+Integer.toHexString(b));
    }
